    private void salirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salirActionPerformed
        // Crea un nuevo fichero con los datos introducidos en los formularios
        String sFichero = "Database.txt";
        File fichero = new File(sFichero);

         try{
         BufferedWriter bw = new BufferedWriter(new FileWriter(sFichero));

            for (Persona persona : PoolPersonas.poolPersonas){
                bw.write("\n-----------------------------------------");
                bw.write("\nNom: " + persona.getNombre());
                bw.write("\nDNI/NIE: " + persona.getDninie());
                bw.write("\nSexe: " + persona.getSexo());
                bw.write("\nEstat: " + persona.getEstadoPersona());
                bw.write("\n-----------------------------------------");
            }
            bw.close();
            } catch (IOException ioe){
                ioe.printStackTrace();
            }
        
        JOptionPane.showMessageDialog(this, "Información guardada en Database.txt");
        System.exit(0);
    }//GEN-LAST:event_salirActionPerformed

