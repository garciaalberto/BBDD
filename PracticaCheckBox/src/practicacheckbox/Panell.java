    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        
        if (nombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Inserte su nombre");
        }
        
        else if (id.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Inserte su identificador");
        } 
        
        else if (!parado.isSelected() && !trabajador.isSelected() && !estudiante.isSelected()){
            JOptionPane.showMessageDialog(this, "Elige una ocupación");
        }
        
        else if (!Validador.checkEstructura(id.getText())){
            JOptionPane.showMessageDialog(this, "Inserta el DNI/NIE en un formato válido");
        }
        else {

            String nombreCompleto = nombre.getText();
            String identifier = id.getText();
            String sex = sexo.getSelectedItem().toString();
            Persona.Estado estado = null;
            if (parado.isSelected()){
                estado = Persona.Estado.ATUR;
            }
            if (trabajador.isSelected()){
                estado = Persona.Estado.TREBALLADOR;
            }
            if (estudiante.isSelected()){
                estado = Persona.Estado.ESTUDIANT;
            }
            
            Persona persona = new Persona (nombreCompleto, identifier, sex, estado);
            PoolPersonas.addPersona(persona);
            
            JOptionPane.showMessageDialog(this, "La información ha sido guardada en la pool");
        }        
    }//GEN-LAST:event_aceptarActionPerformed

